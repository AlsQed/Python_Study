# %s强制转换类型为str
# %d强制转换类型为int
# %d %6d %.6d %-6d %.61在Python和c语言中的用法相司
# %d表示整数输出
# %6d表示输出6位整数，如果不足6位整数，则左边补空格
# %.6d也表示输出6为整数，如果不足6为整数，则左边补0
# %-6d表示输出6位整数，若不足6位，则右边补空格
# %6表示输出结果小数点后保留6位，如果小数点后不足6位，则右边补0
# %f强制转换类型为float
# %.1f表示一位小数
age = 18
AGE = '18'
print('age is:%d'%age)
print('AGE is:%s'%AGE)